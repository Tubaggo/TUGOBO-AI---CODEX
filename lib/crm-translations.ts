export function getCrmI18n(locale: Locale = defaultLocale) {
  const copy = crmTranslations[locale];
  const localeTag = localeTags[locale];

  return {
    locale,
    copy,
    formatDate(value: string | null | undefined, options?: Intl.DateTimeFormatOptions) {
      if (!value) return null;
      return new Intl.DateTimeFormat(localeTag, options ?? { day: "2-digit", month: "2-digit", year: "numeric" }).format(new Date(value));
    },
    formatDateTime(value: string | null | undefined, options?: Intl.DateTimeFormatOptions) {
      if (!value) return null;
      return new Intl.DateTimeFormat(localeTag, options ?? { day: "2-digit", month: "short", hour: "2-digit", minute: "2-digit" }).format(new Date(value));
    },
    formatChannel(channel: ChannelType) {
      return copy.channels[channel];
    },
    formatStatus(status: ConversationStatus | LeadStatus | ReservationStatus) {
      return copy.statuses[status];
    },
    formatIntent(intent: ConversationIntent | AiDetectedIntent | null) {
      if (!intent) return copy.dashboard.generalInquiry;
      return copy.ai.intents[intent];
    },
    formatQualification(status: AiLeadQualificationStatus) {
      return copy.ai.qualification[status];
    },
    formatAction(action: AiRecommendedAction) {
      return copy.ai.actions[action];
    },
    formatReplyType(type: "offer" | "clarification" | "handoff" | "follow_up") {
      return copy.ai.replyTypes[type];
    },
    formatMissingField(field: MissingInfoField) {
      return copy.ai.missingFields[field];
    },
    formatReadiness(value: "low" | "medium" | "high") {
      return copy.ai.readiness[value];
    },
    formatPriority(priority: LeadPriority) {
      return copy.leads.priorities[priority];
    },
    formatSender(sender: MessageSenderType) {
      return copy.conversations.senders[sender];
    },
    formatLanguageCode(language: string | null | undefined) {
      if (!language) return copy.common.notCaptured;
      return copy.languages[language as keyof typeof copy.languages] ?? language.toUpperCase();
    },
    formatResponseStyle(style: ResponseStyle) {
      return copy.settings.responseStyles[style];
    },
    formatKnowledgeBaseCategory(category: KnowledgeBaseCategory) {
      return copy.settings.kbCategories[category];
    },
    formatFlowType(type: AutomationFlowType) {
      return copy.settings.flowTypes[type];
    },
    formatScenario(id: DemoScenarioId) {
      return copy.conversations.scenarios[id];
    },
    formatMissingDetails(count: number) {
      return copy.ai.missingDetails.replace("{count}", String(count));
    },
  };
}
